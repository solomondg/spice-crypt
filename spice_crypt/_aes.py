    # --- GF(2^8) helpers -------------------------------------------------

    def _xtime(a):
        """Multiply *a* by x in GF(2^8) with the AES polynomial."""
        return ((a << 1) ^ 0x1B) & 0xFF if a & 0x80 else (a << 1) & 0xFF

    def _gf_mul(a, b):
        """Multiply *a* and *b* in GF(2^8)."""
        result = 0
        t = a
        for _ in range(8):
            if b & 1:
                result ^= t
            t = _xtime(t)
            b >>= 1
        return result

    # --- Build decryption T-tables (Td0 -- Td3) -------------------------
    # Each table maps a byte through InvSubBytes then InvMixColumns.
    # Td1/Td2/Td3 are byte-rotations of Td0.

    def _build_td_tables():
        Td0 = [0] * 256
        Td1 = [0] * 256
        Td2 = [0] * 256
        Td3 = [0] * 256
        for i in range(256):
            s = _INV_SBOX[i]
            e = _gf_mul(0x0E, s)
            b = _gf_mul(0x0B, s)
            d = _gf_mul(0x0D, s)
            n = _gf_mul(0x09, s)
            Td0[i] = (e << 24) | (n << 16) | (d << 8) | b
            Td1[i] = (b << 24) | (e << 16) | (n << 8) | d
            Td2[i] = (d << 24) | (b << 16) | (e << 8) | n
            Td3[i] = (n << 24) | (d << 16) | (b << 8) | e
        return Td0, Td1, Td2, Td3

    _Td0, _Td1, _Td2, _Td3 = _build_td_tables()

    # --- Key schedule helpers --------------------------------------------

    def _sub_word(w):
        """Apply S-box to each byte of a 32-bit word."""
        return (
            (_SBOX[(w >> 24) & 0xFF] << 24)
            | (_SBOX[(w >> 16) & 0xFF] << 16)
            | (_SBOX[(w >> 8) & 0xFF] << 8)
            | _SBOX[w & 0xFF]
        )

    def _rot_word(w):
        """Rotate a 32-bit word left by 8 bits."""
        return ((w << 8) | (w >> 24)) & 0xFFFFFFFF

    def _key_expansion_256(key: bytes) -> list[int]:
        """AES-256 key expansion returning 60 uint32 round-key words."""
        nk = 8  # key length in 32-bit words
        nr = 14  # number of rounds
        total_words = 4 * (nr + 1)  # 60

        w = list(struct.unpack(">8I", key))
        for i in range(nk, total_words):
            temp = w[i - 1]
            if i % nk == 0:
                temp = _sub_word(_rot_word(temp)) ^ _RCON[i // nk - 1]
            elif i % nk == 4:
                temp = _sub_word(temp)
            w.append(w[i - nk] ^ temp)
        return w

    def _invert_key_schedule(w: list[int]) -> list[int]:
        """Apply InvMixColumns to round keys 1--13 for equivalent inverse cipher."""
        inv = list(w)
        for rnd in range(1, 14):
            base = rnd * 4
            for j in range(4):
                v = inv[base + j]
                b0, b1, b2, b3 = (v >> 24) & 0xFF, (v >> 16) & 0xFF, (v >> 8) & 0xFF, v & 0xFF
                inv[base + j] = (
                    _Td0[_SBOX[b0]] ^ _Td1[_SBOX[b1]] ^ _Td2[_SBOX[b2]] ^ _Td3[_SBOX[b3]]
                )
        return inv

    # --- Block decryption ------------------------------------------------

    def _aes_decrypt_block(block: bytes, rk: list[int]) -> bytes:
        """Decrypt one 16-byte block using the equivalent inverse cipher."""
        s0, s1, s2, s3 = struct.unpack(">4I", block)
        mask = 0xFFFFFFFF

        # Initial AddRoundKey (round 14)
        s0 ^= rk[56]
        s1 ^= rk[57]
        s2 ^= rk[58]
        s3 ^= rk[59]

        # Rounds 13 down to 1 (T-table rounds)
        for rnd in range(13, 0, -1):
            base = rnd * 4
            t0 = (
                _Td0[(s0 >> 24) & 0xFF]
                ^ _Td1[(s3 >> 16) & 0xFF]
                ^ _Td2[(s2 >> 8) & 0xFF]
                ^ _Td3[s1 & 0xFF]
                ^ rk[base]
            ) & mask
            t1 = (
                _Td0[(s1 >> 24) & 0xFF]
                ^ _Td1[(s0 >> 16) & 0xFF]
                ^ _Td2[(s3 >> 8) & 0xFF]
                ^ _Td3[s2 & 0xFF]
                ^ rk[base + 1]
            ) & mask
            t2 = (
                _Td0[(s2 >> 24) & 0xFF]
                ^ _Td1[(s1 >> 16) & 0xFF]
                ^ _Td2[(s0 >> 8) & 0xFF]
                ^ _Td3[s3 & 0xFF]
                ^ rk[base + 2]
            ) & mask
            t3 = (
                _Td0[(s3 >> 24) & 0xFF]
                ^ _Td1[(s2 >> 16) & 0xFF]
                ^ _Td2[(s1 >> 8) & 0xFF]
                ^ _Td3[s0 & 0xFF]
                ^ rk[base + 3]
            ) & mask
            s0, s1, s2, s3 = t0, t1, t2, t3

        # Final round (no MixColumns -- use inverse S-box directly)
        t0 = (
            (_INV_SBOX[(s0 >> 24) & 0xFF] << 24)
            | (_INV_SBOX[(s3 >> 16) & 0xFF] << 16)
            | (_INV_SBOX[(s2 >> 8) & 0xFF] << 8)
            | _INV_SBOX[s1 & 0xFF]
        ) ^ rk[0]
        t1 = (
            (_INV_SBOX[(s1 >> 24) & 0xFF] << 24)
            | (_INV_SBOX[(s0 >> 16) & 0xFF] << 16)
            | (_INV_SBOX[(s3 >> 8) & 0xFF] << 8)
            | _INV_SBOX[s2 & 0xFF]
        ) ^ rk[1]
        t2 = (
            (_INV_SBOX[(s2 >> 24) & 0xFF] << 24)
            | (_INV_SBOX[(s1 >> 16) & 0xFF] << 16)
            | (_INV_SBOX[(s0 >> 8) & 0xFF] << 8)
            | _INV_SBOX[s3 & 0xFF]
        ) ^ rk[2]
        t3 = (
            (_INV_SBOX[(s3 >> 24) & 0xFF] << 24)
            | (_INV_SBOX[(s2 >> 16) & 0xFF] << 16)
            | (_INV_SBOX[(s1 >> 8) & 0xFF] << 8)
            | _INV_SBOX[s0 & 0xFF]
        ) ^ rk[3]

        return struct.pack(">4I", t0 & mask, t1 & mask, t2 & mask, t3 & mask)

