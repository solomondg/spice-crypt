def _flat_sbox_to_normalized(flat_table):
    """Convert the LTspice interleaved flat S-box table to normalized format.

    The flat table stores all 8 S-boxes interleaved at stride 9 (with a
    padding byte at index 0 for 1-based indexing).  The normalized format
    is a list of 8 sub-lists, each 64 entries indexed as
    ``sbox[row * 16 + col]``.
    """
    sboxes = []
    for i in range(8):
        box = [0] * 64
        for row in range(4):
            for col in range(16):
                box[row * 16 + col] = flat_table[(36 * col) + (9 * row) + i + 1]
        sboxes.append(box)
    return sboxes


